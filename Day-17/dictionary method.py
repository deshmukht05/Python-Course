# Dictionary Methods:
# clear() : Remove all elements of dictionary
# copy() : Copies dictionary
# fromkeys() : Creates new dictionary from given set
# get() : Returns value of given key, if not found returns None
# has_key() : Returns True if dictionary has specified key
# keys() : Returns dictionary keys
# items() : Returns (key, value) tuple pairs
# setdefault() : Similar to get() but returns default if not found
# update() : Adds dictionary key-value pairs to existing dictionary
# values() : Returns list of dictionary values