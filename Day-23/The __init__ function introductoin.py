# The __init__ function introductoin:
# - Every class has in-built __init__() function which is always executed when the class is being initiated
# - Using __init__() function, we can assign values ot object properties or other operations that are necessary when object is being created. 
# - The __init__() function is called automatically when object is created. 
# - This is constructor of Python class. 
# - It initializes class attributes with values supplied by user while creating instance. 
# - By convention, the first parameter of a method is called self. 
# - The self parameter is a reference to the current instance of the class which is used to access variables belonging to the class. 
# - We can change name of this first parameter but it has to be the first parameter of any function in the class. 