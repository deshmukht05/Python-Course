# A Stack is a linear structure in which items may be added or removed only at one end.
# Stack are also called Last In First Out(LIFO) list.
# A Stack is a list of elements in which an element may be inserted or deleted only at one end, called the TOP of the stack.
# Elements are removed from a stack in the reverse order of that in which they were inserted into the stack.
# Special terminology used for two basic operations associated with stacks.
# 1. "Push" is the term used to insert an element into a stack.
# 2. "Pop" is the term used to delete an element from a stack.