# A Queue is a linear list of elements in which deletion can take place only at one end and insertion can take place at the other end.
# The end at which insertion takes place is called REAR.
# The end at which deletion takes place is called FRONT.
# The term "front" and "rear" are used in describing a linear list only when it is implemented as queue.
# Queue are called First In First Out(FIFO) lists, since the first element in a queue will be the first element out of the queue.
# In queue, the order in which elements are neter a queue is the order in which they leave.
# This contrasts with stacks, which are last in first out(LIFO) lists.