# The isinstance() function
# Syntax:
# isinstance(object, type)
# where object is the name of object to test and type is a type or class or tuple.

# It returns TRUE if the specified object is of specified type, otherwise returns FALSE.

# Example:
# >>> isintance(5, int)
# >>> True

# >>> isinstance("obj", (float, int, str, list, tuple, dict))
# >>> True