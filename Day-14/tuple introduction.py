# Tuple is very much similar to list with difference that-
# a] tuple is immutable i.e. we cannot change the elements of tuple whereas in list, elements can be changed
# b] tuple elements are in paratheses whereas list elements are in square brackets

# It is an immutable sequence used to store collections of heterogeneous data and are also used for cases where an immutable sqquence of homogeneous data is required.
# Being immutable, iterating through tuple is faster than list.
# Being immutable, tuple can be used as key for a dictionary whereas this is not possible with list.
# Being immutable, tuple data is always write-protected.
