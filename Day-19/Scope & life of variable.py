# Parameters and variables defined inside a function are accessible only inside the function and hence they are called local variables of function are have local scope.
# Lifetime of variable is nothing but the period throughout which variable exists in memory.
# Variables defined inside the function have lifetime as long as function executes.
# Local variables and arguments destroyed once control returns from function.
# Variables defined outside the function are by default global.
# Using global keyword we can make variables global which are defined inside the function.