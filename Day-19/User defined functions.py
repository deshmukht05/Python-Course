# Keyword def followed by parantheses () is used to define user-defined function
# Parantheses contains function arguments, if any.
# The code block i.e. functionality code within every function starts with colon(:) and is indented.
# First statement of a function is the documentation string of the function or docstring and is optional.
# The return statemnt may contain expression if it has to return value to calling function.
# Syntax:
# def function_name(arguments):
#       """docstring"""
#       statement(s)
# Note that, function_name is unique and is uniquely identify the function.
# Function arguments are the values that are passed to function body.
# Statements must follow indentation.